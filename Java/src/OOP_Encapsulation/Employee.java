package OOP_Encapsulation;

public class Employee {
	private String name;
	private int age;

	// setter method:
	public void setName(String name) {
		this.name = name;
	}

	// getter method
	public String getName() {
		return name;
	}
	// setter method:
	public void setAge(int age) {
		this.age = age;
	}
	// getter method
	public int getAge() {
		return age;
	}

}
